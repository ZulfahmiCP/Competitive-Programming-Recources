struct PrefSum {
    int N, M;
    vector<vector<int>> sum;

    PrefSum(const vector<string> &A) : N(sz(A)), M(sz(A[0])), sum(N + 1, vector<int>(M + 1, 0)) {
        for(int i = 1; i <= N; i++)
            for(int j = 1; j <= M; j++)
                sum[i][j] = sum[i - 1][j] + sum[i][j - 1] - 
                            sum[i - 1][j - 1] + (A[i - 1][j - 1] == '*');
    }

    int query(int r1, int c1, int r2, int c2) {
        return sum[r2][c2] - sum[r2][c1 - 1] - sum[r1 - 1][c2] + sum[r1 - 1][c1 - 1];
    }
};

