int main(){
 
    FastIO();
    int n,k,m(11); cin >> n >> k;
    vector<int> A(n), powTen(m, 1);
    vector<vector<int>> count(m);

    auto digit = [&](int x) -> int {
        int d(0);
        while(x > 0)
            x /= 10, d++;
        return d;
    };

    for(int &a : A){
        cin >> a;
        count[digit(a)].pb(a % k);
    }

    for(int i = 0; i < m; i++)
        sort(all(count[i]));
    for(int i = 1; i < m; i++)
        powTen[i] = powTen[i - 1] * 10 % k;

    ll ans(0);
    for(int i = 0; i < n; i++)
        for(int j = 1, p,q; j < m; j++){
            p = (A[i] * (ll)(powTen[j])) % k;
            q = (k - p) % k;
            
            auto l = lower_bound(all(count[j]), q);
            auto r = upper_bound(all(count[j]), q);

            ans += (r - l);

            if(digit(A[i]) == j && (p + A[i] % k) % k == 0)
                ans--;
        }

    cout << ans << '\n';

    return 0;
}
 
