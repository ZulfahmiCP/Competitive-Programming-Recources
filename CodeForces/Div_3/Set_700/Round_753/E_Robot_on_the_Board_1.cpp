int main(){
 
    FastIO();
    int t,m,n,bx,by,min_bx,max_bx,min_by,max_by; cin >> t;
    string command;
    while (t--) {
        cin >> m >> n >> command;

        bx = by = min_bx = max_bx = min_by = max_by = 0;
        for (char c : command) {
            if(c == 'L') 
                min_by = min(min_by, --by);
            else if(c == 'R') 
                max_by = max(max_by, ++by);
            else if(c == 'U') 
                min_bx = min(min_bx, --bx);
            else
                max_bx = max(max_bx, ++bx);

            if(max_bx - min_bx >= m){
                if (bx == min_bx) 
                    min_bx++;
                break;
            }

            if (max_by - min_by >= n) {
                if (by == min_by) 
                    min_by++;
                break;
            }
        }

        cout << 1 - min_bx << ' ' << 1 - min_by << '\n';
    }

    return 0;
}
 
