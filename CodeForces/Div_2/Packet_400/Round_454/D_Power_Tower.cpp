int totient(int n) {
    if(mp.count(n)) 
        return mp[n];

    int phi = n, x = n;
    for(int i = 2; i * i <= n; i++){
        if(n % i) 
            continue;

        phi /= i, phi *= (i - 1);
        while(!(n % i)) 
            n /= i;
    }

    if(n > 1)
        phi /= n, phi *= (n - 1);
    
    return mp[x] = phi;
}

int M(ll n, int m) {
    return (n < m ? n : n % m + m);
}

int power(int a, int b, int m) {
    int c = 1;
    for(;b > 0; a = M(1LL * a * a, m), b >>= 1)
        if(b & 1) c = M(1LL * c * a, m);
    return c;
}

int pow_tow(int l, int r, int m) {
    if(l == r) return M(A[l], m);
    if(m == 1) return 1;
    return power(A[l], pow_tow(l + 1, r, totient(m)), m);
}

