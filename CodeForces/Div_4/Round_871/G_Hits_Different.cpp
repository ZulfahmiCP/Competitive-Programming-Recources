int main(){
 
    FastIO();
    ll t,n,mx(1500); cin >> t;
    vector<vector<ll>> dp(mx + 1, vector<ll>(mx + 1, 0));
    vector<ll> ans(1e6);

    auto val = [&](int x) -> int {
        return x*(x + 1)/2;
    };

    for(int i = 1, cur; i <= mx; i++)
        for(int j = 1; j <= mx; j++){
            cur = val(i - 1) + 1 + val(i + j - 1) - val(i);
            dp[i][j] = dp[i - 1][j] + dp[i][j - 1] - dp[i - 1][j - 1] + 1LL * cur * cur;
            if(cur > 1e6)
                break;
            ans[cur] = dp[i][j];
        }

    while(t--){
        cin >> n;
        cout << ans[n] << '\n';
    }

    return 0;
}
 
