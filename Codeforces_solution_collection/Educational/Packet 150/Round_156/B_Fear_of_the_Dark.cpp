int main(){
 
    FastIO();
    int t; cin >> t;
    pair<double, double> p,a,b,o{0, 0};
    double ans,pa,pb,oa,ob,ab;

    auto dist = [&](pair<double, double> x, pair<double, double> y) -> double {
        return sqrt((x.X - y.X) * (x.X - y.X) + (x.Y - y.Y) * (x.Y - y.Y));
    };

    while(t--){
        cin >> p.X >> p.Y >> a.X >> a.Y >> b.X >> b.Y;

        pa = dist(p, a), pb = dist(p, b);
        oa = dist(o, a), ob = dist(o, b);
        ab = dist(a, b), ans = 1e9;

        ans = min(ans, max(pa, oa));
        ans = min(ans, max(pb, ob));
        ans = min(ans, max({ab/2, pa, ob}));
        ans = min(ans, max({ab/2, pb, oa}));


        cout << fixed << setprecision(10) << ans << '\n';
    }

    return 0;
}
 
