int main(){
 
    FastIO();
    ll n,k,m; cin >> n >> k >> m;

    if(k > m || (n - 1) * k < m){
        cout << "NO\n";
        return 0;
    }

    cout << "YES\n";

    int diff, prev = 1, need = m % k;
    for(int i = 0; i < k; i++){ 
        diff = m / k + ((need--) > 0 ? 1 : 0);
        
        if(prev + diff <= n)
            prev += diff;
        else if(prev - diff > 0) 
            prev -= diff;
        else 
            assert(false);

        cout << prev << " \n"[i == k - 1];
    }
  
    return 0;
}
 
