int main(){
 
    FastIO();
    int t,n; cin >> t;
    ll ans,mx,mn;
    while(t--){
        cin >> n;
        vector<int> A(n);
        vector<ll> pref(n + 1);

        for(int &a : A)
            cin >> a;

        ans = *max_element(all(A)) - *min_element(all(A));

        for(int i = 1; i <= n; i++)
            pref[i] = pref[i - 1] + A[i - 1];

        for(int i = 2; i * i <= n; i++){
            if(n % i)
                continue;

            mn = 1e18, mx = -1e18;
            for(int j = i; j <= n; j += i){
                mn = min(mn, pref[j] - pref[j - i]);
                mx = max(mx, pref[j] - pref[j - i]);
            }

            ans = max(ans, mx - mn);

            mn = 1e18, mx = -1e18;
            for(int j = n/i; j <= n; j += n/i){
                mn = min(mn, pref[j] - pref[j - n/i]);
                mx = max(mx, pref[j] - pref[j - n/i]);
            }
            
            ans = max(ans, mx - mn);
        }

        cout << ans << '\n';
    }

    return 0;
}
 
