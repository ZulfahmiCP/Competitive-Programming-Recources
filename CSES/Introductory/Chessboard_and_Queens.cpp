int main(){
 
    FastIO();
    int n = 8, ans = 0;
    vector<string> S(n);

    for(auto &s : S)
        cin >> s;

    vector<int> Q(n);
    iota(all(Q), 0);

    auto can_place = [&]() -> bool {
        vector<bool> A(4 * n, 0), B(4 * n, 0);
        for(int i = 0; i < n; i++){
            if(S[i][Q[i]] == '*')
                return 0;
            if(A[i + Q[i]])
                return 0;
            if(B[i - Q[i] + n - 1])
                return 0;

            A[i + Q[i]] = 1;
            B[i - Q[i] + n - 1] = 1;
        }

        return 1;
    };  

    do {
        ans += can_place();
        for(int i = 0; i < n; i++)
            if(S[i][Q[i]] == '-')
                S[i][Q[i]] = '.';
    } while(next_permutation(all(Q)));

    cout << ans << '\n';

    return 0;
}
 
